    public void SlideShow(){
        
        new Thread(){
            int count;
            
            @Override
            public void run(){
                try{
                    while(true){
                        switch(count){
                            case 0:
                                
                                ImageIcon img1 = new ImageIcon(getClass().getResource("/resized/slider_1.jpg"));
                                jLabel1.setIcon(img1);
                                
                                Thread.sleep(3000);
                                
                                ac.jLabelXLeft(0, -590, 20, 5, jLabel1);
                                ac.jLabelXLeft(590, 0, 20, 5, jLabel2);
                                
                                count = 1;
                                break;
                                
                            case 1:
                                
                                ImageIcon img2 = new ImageIcon(getClass().getResource("/resized/slider_2.jpg"));
                                jLabel2.setIcon(img2);
                                
                                Thread.sleep(3000);
                                
                                ac.jLabelXRight(-590, 0, 20, 5, jLabel1);
                                ac.jLabelXRight(0, 590, 20, 5, jLabel2);

                                count = 2;
                                break;
                                
                            case 2:

                                ImageIcon img3 = new ImageIcon(getClass().getResource("/resized/slider-3.jpg"));
                                jLabel1.setIcon(img3);
                                
                                Thread.sleep(3000);
                                
                                ac.jLabelXLeft(0, -590, 20, 5, jLabel1);
                                ac.jLabelXLeft(590, 0, 20, 5, jLabel2);

                                count = 3;
                                break;
                                
                            case 3:

                                ImageIcon img4 = new ImageIcon(getClass().getResource("/resized/slider_4.jpg"));
                                jLabel2.setIcon(img4);
                                
                                Thread.sleep(3000);
                                ac.jLabelXRight(-590, 0, 20, 5, jLabel1);
                                ac.jLabelXRight(0, 590, 20, 5, jLabel2);

                                count = 4;
                                break;
                                
                            case 4:

                                ImageIcon img5 = new ImageIcon(getClass().getResource("/resized/slider_5.jpg"));
                                jLabel1.setIcon(img5);
                                
                                Thread.sleep(3000);
                                ac.jLabelXLeft(0, -590, 20, 5, jLabel1);
                                ac.jLabelXLeft(590, 0, 20, 5, jLabel2);

                                count = 5;
                                break;
                                
                            case 5:

                                ImageIcon img6 = new ImageIcon(getClass().getResource("/resized/slider_6.jpg"));
                                jLabel2.setIcon(img6);
                                
                                Thread.sleep(3000);
                                ac.jLabelXRight(-590, 0, 20, 5, jLabel1);
                                ac.jLabelXRight(0, 590, 20, 5, jLabel2);

                                count = 0;
                                break;
                        }
                    }
                }catch(Exception e){
                    
                }
            }
        }.start();
    }
    
