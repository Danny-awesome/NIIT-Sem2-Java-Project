        Timer timer = new Timer(100, new ActionListener() {
            int i = 0;

            public void actionPerformed(ActionEvent evt) {
                jProgressBar1.setValue(i);
                i++;
                progressBarPanel.repaint();
                if (i == 100) {
                    ((Timer) evt.getSource()).stop();
                    dispose();
                    new LoginForm().setVisible(true);
                }
            }
        });
