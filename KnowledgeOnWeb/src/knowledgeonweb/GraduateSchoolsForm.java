    void readDB() {
        try {
            Class.forName("org.sqlite.JDBC");
            Connection conn = DriverManager.getConnection("jdbc:sqlite:KnowledgeOnWebDB.db");
            Statement stat = conn.createStatement();
            String sqlselect = "select schoolCode, schoolName, contactName, contactPhone,"
                    + "email, programName, noOfSemesters, curriculum, prerequisites from GraduateSchoolsData";
            PreparedStatement pst = conn.prepareStatement(sqlselect);
            ResultSet rs = pst.executeQuery();
            gradSchoolTable.setModel(DbUtils.resultSetToTableModel(rs));
            TableColumnModel tcm = gradSchoolTable.getColumnModel();
            tcm.getColumn(0).setHeaderValue("School code");
            tcm.getColumn(1).setHeaderValue("School name");
            tcm.getColumn(2).setHeaderValue("Contact person");
            tcm.getColumn(3).setHeaderValue("Contact phone");
            tcm.getColumn(4).setHeaderValue("E-Mail");
            tcm.getColumn(5).setHeaderValue("Program name");
            tcm.getColumn(6).setHeaderValue("No of semesters");
            tcm.getColumn(7).setHeaderValue("Curriculum");
            tcm.getColumn(8).setHeaderValue("Pre-requisites");

        } catch (ClassNotFoundException | SQLException e) {
            System.out.println(e.getMessage());
            e.printStackTrace();
        }
    }

