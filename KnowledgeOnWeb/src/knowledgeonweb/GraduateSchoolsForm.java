    private void gradSchoolTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_gradSchoolTableMouseClicked
        // TODO add your handling code here:
        int row = gradSchoolTable.getSelectedRow();
        String Table_clicked = (gradSchoolTable.getModel().getValueAt(row, 0).toString());

        try {
            Class.forName("org.sqlite.JDBC");
            Connection conn = DriverManager.getConnection("jdbc:sqlite:KnowledgeOnWebDB.db");
            String query = "select * from GraduateSchoolsData where schoolcode = '" + Table_clicked + "' ";
            PreparedStatement pst1 = conn.prepareStatement(query);
            ResultSet rs1 = pst1.executeQuery();

            while (rs1.next()) {
                String add1 = rs1.getString("schoolCode");
                getcode.setText(add1);
                String add2 = rs1.getString("schoolName");
                getschoolname.setText(add2);
                String add3 = rs1.getString("contactName");
                getContactname.setText(add3);
                String add4 = rs1.getString("contactPhone");
                getphone.setText(add4);
                String add5 = rs1.getString("email");
                getemail.setText(add5);
                String add6 = rs1.getString("programName");
                getprogram.setText(add6);
                String add7 = rs1.getString("noOfSemesters");
                getsemesters.setText(add7);

            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_gradSchoolTableMouseClicked

